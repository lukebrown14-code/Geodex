export function searchCountries(query: string): CountryData[] {
  const q = query.toLowerCase().trim();
  if (!q) return [];
  return Object.values(countries).filter(
    (c) =>
      c.name.toLowerCase().includes(q) ||
      c.code.toLowerCase() === q ||
      c.region.toLowerCase().includes(q),
  );
}

export function getCountryByCode(code: string): CountryData | undefined {
  return countries[code.toUpperCase()];
}

export function getAllCountries(): CountryData[] {
  return Object.values(countries);
}

export function formatNumber(num: number): string {
  if (num >= 1_000_000_000) return (num / 1_000_000_000).toFixed(2) + "B";
  if (num >= 1_000_000) return (num / 1_000_000).toFixed(1) + "M";
  if (num >= 1_000) return (num / 1_000).toFixed(1) + "K";
  return num.toLocaleString();
}

export function formatCurrency(num: number): string {
  if (num >= 1_000_000) return "$" + (num / 1_000_000).toFixed(2) + "T";
  if (num >= 1_000) return "$" + (num / 1_000).toFixed(1) + "B";
  return "$" + num.toLocaleString() + "M";
}
